    private void selectData() {
        String kolom[] = {"ID Barang","Nama Barang","Banyak Barang","Harga Barang","Jumlah"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        int transaksi = Integer.parseInt(jtTransaksi.getText());
        String SQL = "SELECT id_barang,nama_barang,banyak_barang,harga_barang,jumlah FROM tb_transaksi where id_transaksi='"+transaksi+"'";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String id= rs.getString(1);
                String nama= rs.getString(2);
                String banyak= rs.getString(3);
                String harga= rs.getString(4);
                String jumlah= rs.getString(5);
                String data[] = {id, nama,banyak,harga, jumlah};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        jtBarang.setModel(dtm);
    }

    private void setJam() {
    {
        ActionListener taskPerformer = new ActionListener()
        {
            public void actionPerformed(ActionEvent evt)
            {
                Date dt = new Date();
                int nilai_jam = dt.getHours();
                int nilai_menit = dt.getMinutes();
                int nilai_detik = dt.getSeconds();
                String nol_jam = "";
                String nol_menit = "";
                String nol_detik = "";
                if(nilai_jam<=9)
                {
                    nol_jam="0";
                }
                if(nilai_menit<=9)
                {
                    nol_menit="0";
                }
                if(nilai_detik<=9)
                {
                    nol_detik="0";
                }
                String jam = nol_jam+Integer.toString(nilai_jam);
                String menit = nol_menit+Integer.toString(nilai_menit);
                String detik = nol_detik+Integer.toString(nilai_detik);
                jlJam.setText(jam+":"+menit+":"+detik);
            }
        };
        new Timer(100, taskPerformer).start();
    } //To change body of generated methods, choose Tools | Templates.
    }

