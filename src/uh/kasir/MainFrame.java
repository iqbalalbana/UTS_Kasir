    private void jbTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbTambahActionPerformed
        // TODO add your handling code here:
        int transaksi = Integer.parseInt(jtTransaksi.getText());
        int banyak = Integer.parseInt(jtBanyak.getText());
        int harga = Integer.parseInt(jtHarga.getText());
        int jumlah = banyak * harga;
        if ("".equals(jtNama.getText()) || "".equals(jtBanyak.getText()) || "".equals(jtHarga.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            
            String SQL = "INSERT INTO tb_transaksi (id_transaksi, id_barang,nama_barang,banyak_barang,harga_barang,jumlah) "
                    + "VALUES(" + transaksi+ ",'" + jtID.getText() + "','" + jtNama.getText() + "'," + banyak + "," + harga + ","
                    + jumlah + ")";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","Sukses",JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbTambahActionPerformed

